    'fi',
    'cat "$output_file"',
    'if [ -s "$timeout_marker" ]; then',
    '  echo "The focused test command exceeded 120 seconds; this is not bug evidence." >&2',
    '  exit 1',
    'fi',
    '_head_after=$(git rev-parse --verify HEAD) || { echo "Could not inspect HEAD after the focused test." >&2; exit 2; }',
    'git ls-files --stage > "$_index_after" || { echo "Could not inspect the Git index after the focused test." >&2; exit 2; }',
    'if [ "$_head_after" != "$_head_before" ] || ! cmp -s "$_index_before" "$_index_after"; then',
    '  echo "The workspace changed while the focused test was running (HEAD or index changed)." >&2',
    '  exit 1',
    'fi',
    '[ -f "$changed_test" ] && [ ! -L "$changed_test" ] || { echo "The changed test disappeared or became a symlink while it was running." >&2; exit 1; }',
