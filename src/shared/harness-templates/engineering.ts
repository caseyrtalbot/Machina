    'if [ "$_test_hash_after" != "$_test_hash_before" ]; then',
    '  echo "The changed test rewrote itself while it was running; this is not stable bug evidence." >&2',
    '  exit 1',
    'fi',
    'assert_only_changed_test "$changed_test" "$_changes_tmp"',
    '_post_guard_status=$?',
    'if [ "$_post_guard_status" -eq 2 ]; then',
    '  echo "Could not inspect the workspace after the focused test; this is not bug evidence." >&2',
    '  exit 2',
    'fi',
    'if [ "$_post_guard_status" -ne 0 ]; then',
    '  echo "The workspace changed while the focused test was running." >&2',
    '  exit 1',
    'fi',
    'if [ "$status" -eq 0 ]; then',
    '  echo "The changed test passed; it does not reproduce the reported bug." >&2',
    '  exit 1',
    'fi',
    'if [ "$status" -eq 2 ] || [ "$status" -eq 126 ] || [ "$status" -eq 127 ]; then',
    '  echo "The test command could not run; this is not bug evidence." >&2',
    '  exit 1',
    'fi',
    'if grep -Eiq \'no tests? (found|collected)|no test files found|missing script|no test specified|command not found|cannot find module|no module named|modulenotfounderror|pytest is unavailable|failed to resolve|no such file or directory|unknown file extension|syntax[[:space:]]*error|setup failed|failed to load (config|configuration)|error: failed to|error during build|build failed|compilation failed|could not compile|build script\' "$output_file"; then',
    '  echo "The observed failure is test discovery or infrastructure, not accepted bug evidence." >&2',
    '  exit 1',
    'fi',
    '_runner_kind=$(cat "$_runner_kind_file" 2>/dev/null) || { echo "The focused runner did not identify itself; this is not bug evidence." >&2; exit 1; }',
    'case "$_runner_kind" in',
    '  node|tsx)',
    '    _node_executed=false',
    '    if grep -Eq \'^# Subtest: .+\' "$output_file" && grep -Eq \'^not ok [0-9]+ - .+\' "$output_file"; then',
    '      _node_executed=true',
    '    fi',
    '    if grep -Eq \'tests[[:space:]]+[1-9][0-9]*\' "$output_file" && grep -Eq \'fail[[:space:]]+[1-9][0-9]*\' "$output_file" && grep -F -- "test at $changed_test:" "$output_file" >/dev/null 2>&1; then',
    '      _node_executed=true',
    '    fi',
    '    if [ "$_node_executed" != true ]; then',
    '      echo "The JavaScript test body did not execute and fail; suite-load errors are not bug evidence." >&2',
    '      exit 1',
    '    fi',
    '    if grep -F -- "# Subtest: $_repo_root/$changed_test" "$output_file" >/dev/null 2>&1; then',
    '      echo "The JavaScript file failed to load before a test body executed." >&2',
    '      exit 1',
    '    fi',
    '    ;;',
