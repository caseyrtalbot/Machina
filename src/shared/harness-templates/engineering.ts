    'if ! changed_tests=$(collect_changed_paths "$_changes_tmp" test tests \\',
    "  ':(glob)**/test/**' ':(glob)**/tests/**' ':(glob)**/__tests__/**' ':(glob)**/*.test.*' ':(glob)**/*.spec.*' ':(glob)**/*_test.go' ':(glob)**/test_*.py'); then",
