const BUG_CHANGE_GUARD = [
  'collect_changed_paths() {',
  '  _collect_out=$1',
  '  shift',
  '  : > "$_collect_out" || return 2',
  '  git diff --no-renames --name-only -- "$@" > "$_collect_out" || return 2',
  '  git diff --cached --no-renames --name-only -- "$@" >> "$_collect_out" || return 2',
  '  git ls-files --others --exclude-standard -- "$@" >> "$_collect_out" || return 2',
  '  LC_ALL=C sort -u "$_collect_out"',
  '}',
  'assert_only_changed_test() {',
  '  _allowed_test=$1',
  '  _guard_tmp=$2',
  '  _all_changes=$(collect_changed_paths "$_guard_tmp") || return 2',
  '  : > "$_guard_tmp" || return 2',
  '  while IFS= read -r _changed; do',
  '    [ -n "$_changed" ] || continue',
  '    [ "$_changed" = "$_allowed_test" ] && continue',
  '    case "$_changed" in',
  '      "$_harness_rel/"*) continue ;;',
  '    esac',
  '    printf "%s\\n" "$_changed" >> "$_guard_tmp" || return 2',
  '  done <<EOF',
  '$_all_changes',
  'EOF',
  '  if [ -s "$_guard_tmp" ]; then',
  '    echo "Bug reproducer must change exactly one test and no other workspace file outside its own harness directory:" >&2',
  '    cat "$_guard_tmp" >&2',
  '    return 1',
  '  fi',
  '  return 0',
  '}',
  'assert_unique_substring_target() {',
  '  _exact_test=$1',
  '  _target_tmp=$2',
  '  git ls-files --cached --others --exclude-standard > "$_target_tmp" || return 2',
  '  while IFS= read -r _candidate; do',
  '    [ -n "$_candidate" ] || continue',
  '    [ "$_candidate" = "$_exact_test" ] && continue',
  '    case "$_candidate" in',
  '      *"$_exact_test"*)',
  '        echo "The selected runner cannot isolate the changed test path exactly; it also matches $_candidate." >&2',
  '        return 2',
  '        ;;',
  '    esac',
  '  done < "$_target_tmp"',
  '  return 0',
  '}',
  'collect_process_tree() {',
  '  for _child_pid in $(pgrep -P "$1" 2>/dev/null || :); do',
  '    collect_process_tree "$_child_pid"',
  '  done',
  '  printf "%s\\n" "$1"',
  '}',
  'terminate_process_tree() {',
  '  _process_tree=$(collect_process_tree "$1") || return 2',
  '  for _kill_pid in $_process_tree; do',
  '    kill -TERM "$_kill_pid" 2>/dev/null || :',
  '  done',
  '  sleep 1 </dev/null >/dev/null 2>&1',
  '  for _kill_pid in $_process_tree; do',
  '    kill -KILL "$_kill_pid" 2>/dev/null || :',
  '  done',
  '  return 0',
  '}'
].join('\n')

